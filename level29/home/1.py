nums = [2, 4, 0, 9, 5]

nums.insert(1, 3)   # ჩასვამს 3-ს ინდექსზე 1 → [2, 3, 4, 0, 9, 5]

nums.remove(0)      # ამოიღებს პირველ 0-ს სიიდან → [2, 3, 4, 9, 5]

nums.insert(0, nums.count(0))  # დაითვლის 0-ების რაოდენობას (0), ჩასვამს 0-ს ინდექსზე 0 → [0, 2, 3, 4, 9, 5]

print(nums[3])      # nums[3] არის 4